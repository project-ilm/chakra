const C=require("../src/chakra-core.js");const {ok,near,eq,done}=require("./_assert.js");
console.log("ANNUAL EVENTS (computed, 2026)");
const ev=C.annualEvents(2026);
const find=n=>ev.filter(e=>e.name.indexOf(n)>=0).map(e=>e.date);
const within=(ds,lo,hi)=>ds.some(d=>d>=lo&&d<=hi);
ok("Makara Saṅkrānti mid-Jan",within(find("Makara"),"2026-01-13","2026-01-16"));
ok("Holi pūrṇimā = 2026-03-03 (eclipse day)",find("Holi").indexOf("2026-03-03")>=0);
ok("Rāma Navamī late Mar/early Apr",within(find("Rāma Navamī"),"2026-03-24","2026-04-01"));
ok("Buddha Pūrṇimā = 2026-05-01",find("Buddha").indexOf("2026-05-01")>=0);
ok("Rakṣā Bandhana late Aug",within(find("Rakṣā"),"2026-08-26","2026-08-30"));
ok("Janmāṣṭamī early Sep",within(find("Janmāṣṭamī"),"2026-09-02","2026-09-06"));
ok("Vijayadaśamī ~20 Oct",within(find("Vijayadaśamī"),"2026-10-18","2026-10-22"));
ok("Dīpāvalī ~8 Nov",within(find("Dīpāvalī"),"2026-11-07","2026-11-10"));
ok("Pesach = 2026-04-02",find("Pesach").indexOf("2026-04-02")>=0);
ok("Rosh Hashana Sep 2026",within(find("Rosh Hashana"),"2026-09-11","2026-09-13"));
ok("Eid al-Fiṭr ~20 Mar (tabular)",within(find("Eid al-Fiṭr"),"2026-03-18","2026-03-22"));
ok("ʿĀshūrā ~25-26 Jun (both reckonings)",within(find("Āshūrā"),"2026-06-24","2026-06-27"));
ok("Arbaʿīn present (Shia)",find("Arbaʿīn").length===1);
ok("Vaisakhi = 2026-04-14",find("Vaisakhi").indexOf("2026-04-14")>=0);
ok("4 eclipses listed",ev.filter(e=>e.tradition==="Astronomical").length===4);
console.log("  sample:",ev.slice(0,4).map(e=>e.date+" "+e.name).join(" | "));
done("test-events");
